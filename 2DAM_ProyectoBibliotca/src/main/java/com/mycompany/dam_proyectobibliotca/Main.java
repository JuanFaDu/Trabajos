    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String DNI = devolDNI.getText();
        String ID = devolID.getText();
        String val1 ="", val2 = "";
        int fila = 0;
        m = (DefaultTableModel) tablaUsuarios.getModel();
        for (int i=0; i<m.getRowCount(); i++) {
            String a = m.getValueAt(i, 3).toString();
            if (a.equals(DNI)) {
                val1 = a;
            } 
        }
        m = (DefaultTableModel) tablaLibros.getModel();
        for (int i=0; i<m.getRowCount(); i++) {
            String b = m.getValueAt(i, 0).toString();
            if (b.equals(ID)) {
                fila = i;
                val2 = b;
            }
        }
        if (!val1.isBlank() && !val2.isBlank()) {
            if ("1".equals((String) m.getValueAt(fila, 5))) {
                m.setValueAt(0, fila, 5) ;
            } else {
                JOptionPane.showMessageDialog(null, "Libro ya prestado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "DNI o ID no válido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
