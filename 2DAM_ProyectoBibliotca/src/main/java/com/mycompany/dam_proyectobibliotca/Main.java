    void PrepararBD() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/biblioteca_netbeans", "root", "");
            System.out.println("Conectando");
            String query = "SELECT * FROM libros";
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            m.setRowCount(0);
            
            while (rs.next()) {
                int id = rs.getInt("id");
                String titulo = rs.getString("titulo");
                String editorial = rs.getString("editorial");
                String autor = rs.getString("autor");
                int cantidad = rs.getInt("cantidad");
                boolean dispnble = rs.getBoolean("disponible");
                m.addRow(new Object[] {id, titulo, autor, editorial, cantidad, dispnble});
            }
            rs.close();
            con.close();
            stmt.close();
            
        } catch (Exception ex) {
            System.out.println("No conectado. Error: ");
            ex.printStackTrace();
        }
    }
